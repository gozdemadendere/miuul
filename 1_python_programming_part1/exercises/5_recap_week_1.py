### Fonksiyon vs Method farkı nedir?
# Method: belirli görevleri yerine getiren fonksiyonlardır. Mesela listeye eleman eklemek, çıkarmak için kullanılan yapılardır. (list.append, len(list) gibi)
# Methodlar belirli bir Class yapısı içinde tanımlanır, fonksiyonlar ise bağımsızdır.

# Fonksiyon: belirli görevleri yerine getirir, bağımsız bir programlama yapı taşıdır ve herhangi bir nesneye bağlı olmadan çalışabilir.
# Python'da def anahtar kelimesiyle tanımlanan bir fonksiyon, herhangi bir sınıf veya nesne içinde değildir.



### Python, ilgili metodun hangi veri yapısına uygulanabileceğini nereden anlar?
